
package Classes;


public class Operaciones {
    
    public void sumar(int n1, int n2) {
        System.out.println("Resultado: " + n1 + " + " + n2 + " = " + (n1+n2));
    }
    
    public void restar(int n1, int n2) {
        System.out.println("Resultado: " + n1 + " - " + n2 + " = " + (n1-n2));
    }
    
    public void multiplicar(int n1, int n2) {
        System.out.println("Resultado: " + n1 + " * " + n2 + " = " + (n1*n2));
    }
    
    public void dividir(int n1, int n2) {
        System.out.println("Resultado: " + n1 + " / " + n2 + " = " + (n1/n2));
    }
    
}
